async function seedContent() {
  console.log('开始填充基础理论内容...')

  for (const article of articles) {
    const searchText = (article.content as ContentBlock[])
      .map((block) => {
        if (block.type === 'section') return `${block.heading} ${block.body}`
        if (block.type === 'quote') return block.body
        if (block.type === 'list') return `${block.heading || ''} ${block.items.join(' ')}`
        if (block.type === 'table') return `${block.heading || ''} ${block.rows.map((r) => r.join(' ')).join(' ')}`
        return ''
      })
      .join(' ')

    await prisma.article.upsert({
      where: { slug: article.slug },
      update: {
        title: article.title,
        categorySlug: article.categorySlug,
        summary: article.summary,
        content: article.content as object[],
        tags: article.tags,
        references: article.references,
        searchText,
        sortOrder: article.sortOrder,
      },
      create: {
        title: article.title,
        slug: article.slug,
        categorySlug: article.categorySlug,
        summary: article.summary,
        content: article.content as object[],
        tags: article.tags,
        references: article.references,
        searchText,
        sortOrder: article.sortOrder,
      },
    })

    console.log(`  已填充: ${article.title}`)
  }

  // 创建关联关系
  const allArticles = await prisma.article.findMany({
    where: { categorySlug: { startsWith: 'jichu-' } },
    select: { id: true, slug: true },
  })
  const slugToId = new Map(allArticles.map((a) => [a.slug, a.id]))

  const relations: [string, string][] = [
    ['yinyang-xueshuo', 'wuxing-xueshuo'],
    ['yinyang-xueshuo', 'bingji-xueshuo'],
    ['wuxing-xueshuo', 'cangxiang-xueshuo'],
    ['cangxiang-xueshuo', 'qixue-jinye-shen'],
    ['bingyin-xueshuo', 'bingji-xueshuo'],
    ['bingji-xueshuo', 'zhize-zhifa'],
    ['zhongyi-zhexue-siwei', 'yinyang-xueshuo'],
    ['zhongyi-zhexue-siwei', 'wuxing-xueshuo'],
  ]

  for (const [fromSlug, toSlug] of relations) {
    const fromId = slugToId.get(fromSlug)
    const toId = slugToId.get(toSlug)
    if (fromId && toId) {
      await prisma.articleRelation.upsert({
        where: { fromArticleId_toArticleId: { fromArticleId: fromId, toArticleId: toId } },
        update: {},
        create: { fromArticleId: fromId, toArticleId: toId, relationType: 'related' },
      })
    }
  }

  console.log(`基础理论内容填充完成，共 ${articles.length} 篇文章，${relations.length} 个关联关系`)
}

seedContent()
  .catch((e) => {
    console.error('填充失败:', e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
